#[test]
fn valid_cycles() {
    let text = indoc! {r#"
        type Foo: [Foo]
        type Bar: Bar?

        type Rec: rec {
            rec: Rec?
            name: string
            foo: Foo
        }

        type Union: union("type") {
            rec: Rec
        }

        type Response: resp {
            body: Union
        }
    "#};

    let mut diagnostics = vec![];
    let tokens = tokenize(text, &mut diagnostics);
    let res = parse(tokens);

    let mut analyzer = Analyzer::new(&res.root);
    analyzer.validate_symbols();

    assert!(analyzer.reports.is_empty());

    let mut names: Vec<_> = analyzer
        .symbol_map
        .keys()
        .map(|id| analyzer.strings.resolve(*id))
        .collect();
    names.sort();
    assert_eq!(names, vec!["Bar", "Foo", "Rec", "Response", "Union"])
}

#[test]
fn invalid_cycles() {
    let text = indoc! {r#"
        type Foo: rec {
            bar: Bar
        }

        type Bar: union("type") {
            baz: Baz
        }

        type Baz: FooBar

        type FooBar: Foo

        type IgnoredEnum: enum(string) {
            "foo"
        }

        type Resp: resp {
            headers: Foo
        }
    "#};

    let mut diagnostics = vec![];
    let tokens = tokenize(text, &mut diagnostics);
    let res = parse(tokens);

    let mut analyzer = Analyzer::new(&res.root);
    analyzer.validate_symbols();

    // Reports are generated by iterating a HashMap. We sort it to stabilize
    // the order for snapshot purposes.
    analyzer.reports.sort_by(|a, b| a.title.cmp(&b.title));
    insta::assert_debug_snapshot!(analyzer.reports);

    let mut names: Vec<_> = analyzer
        .symbol_map
        .keys()
        .map(|id| analyzer.strings.resolve(*id))
        .collect();
    names.sort();
    assert_eq!(
        names,
        vec!["Bar", "Baz", "Foo", "FooBar", "IgnoredEnum", "Resp"]
    )
}
