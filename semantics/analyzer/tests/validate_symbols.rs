use better_api_syntax::{parse, tokenize};
use indoc::indoc;

use crate::analyzer::Analyzer;

#[test]
fn valid_type_defs() {
    let text = indoc! {r#"
        type Foo: string
        type Bar: i32
        type Baz: Bar
        type Asdf: None
    "#};

    let mut diagnostics = vec![];
    let tokens = tokenize(text, &mut diagnostics);
    let res = parse(tokens);

    let mut analyzer = Analyzer::new(&res.root);
    analyzer.validate_symbols();

    assert!(analyzer.reports.is_empty());

    let mut names: Vec<_> = analyzer
        .symbol_map
        .keys()
        .map(|id| analyzer.strings.resolve(*id))
        .collect();
    names.sort();
    assert_eq!(names, vec!["Asdf", "Bar", "Baz", "Foo"])
}

